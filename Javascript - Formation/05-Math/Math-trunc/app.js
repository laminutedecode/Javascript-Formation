// La fonction Math.trunc() en JavaScript est utilisée pour supprimer la partie décimale d'un nombre et renvoyer la partie entière restante. Elle permet de tronquer un nombre sans effectuer d'arrondi.




console.log(Math.trunc(3.14159));   // Affiche 3 (supprime la partie décimale)
console.log(Math.trunc(-2.8));      // Affiche -2 (supprime la partie décimale)
console.log(Math.trunc(0));         // Affiche 0 (aucune partie décimale à supprimer)


// Dans ces exemples, nous utilisons Math.trunc() pour supprimer la partie décimale des nombres donnés. La fonction renvoie la partie entière du nombre, en supprimant simplement la partie décimale sans effectuer d'arrondi.

// Il est important de noter que Math.trunc() renvoie toujours un nombre de type number, qu'il soit positif ou négatif. Si le nombre donné est déjà un entier, il est renvoyé tel quel sans modification.

// Il convient également de mentionner que Math.trunc() ne modifie pas le nombre d'origine, mais renvoie simplement la partie entière du nombre.

// En résumé, Math.trunc() est une fonction JavaScript utilisée pour supprimer la partie décimale d'un nombre et renvoyer la partie entière restante, sans effectuer d'arrondi.