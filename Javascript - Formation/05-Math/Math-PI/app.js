// La constante Math.PI en JavaScript représente la valeur numérique de Pi (π), une constante mathématique largement utilisée en géométrie, en trigonométrie et dans de nombreux autres domaines des mathématiques et des sciences.

// La valeur de Math.PI est approximativement égale à 3.141592653589793. Elle représente le rapport entre la circonférence d'un cercle et son diamètre. En d'autres termes, Pi est le nombre qui, multiplié par le diamètre d'un cercle, donne sa circonférence.

// La constante Math.PI est prédéfinie dans JavaScript, ce qui signifie qu'elle est disponible dès le chargement de l'environnement JavaScript, sans avoir besoin de la déclarer ou de l'initialiser.

// Voici un exemple d'utilisation de Math.PI :


var rayon = 5;
var circonference = 2 * Math.PI * rayon;

console.log(circonference); // Affiche la circonférence d'un cercle de rayon 5
// Dans cet exemple, nous utilisons Math.PI pour accéder à la valeur de Pi. Nous multiplions ensuite cette valeur par le rayon d'un cercle (5) et par 2 pour obtenir la circonférence du cercle. Le résultat est affiché, correspondant à la circonférence d'un cercle de rayon 5, soit environ 31.41592653589793.

// En résumé, Math.PI est une constante JavaScript qui représente la valeur numérique de Pi (π). Elle est utilisée dans divers calculs mathématiques et scientifiques, notamment en géométrie et en trigonométrie.