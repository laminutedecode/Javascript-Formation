// La méthode reverse() est utilisée pour inverser l'ordre des éléments d'un tableau. Elle modifie le tableau d'origine en le plaçant dans l'ordre inverse.

// La syntaxe de reverse() est simplement array.reverse().

// Exemple :


let fruits = ['pomme', 'banane', 'orange', 'kiwi'];
fruits.reverse();
console.log(fruits);  // affiche ['kiwi', 'orange', 'banane', 'pomme']


// Dans cet exemple, la méthode reverse() est appelée sur le tableau fruits. Elle inverse l'ordre des éléments du tableau d'origine, modifiant ainsi le tableau lui-même. Après l'exécution de reverse(), les éléments de fruits sont maintenant dans l'ordre inverse.

// Il est important de noter que reverse() modifie le tableau d'origine et ne renvoie pas de nouveau tableau.

// La méthode reverse() est utile lorsque vous avez besoin de modifier l'ordre des éléments d'un tableau existant de manière permanente, sans créer de nouveau tableau.

